package com.hfut.domain;
/**
 * Algorithm
 * @author rdy
 * @time 2016-12-22
 */
public class OpenAlgorithm {

	private String algorithm_name;
	private String algorithm_type;
	private String algorithm_tool;
	private String algorithm_language;
	private String algorithm_parameter;
	private String algorithm_data_format;
	private String algorithm_method;
	private String img_id;
	
	public OpenAlgorithm() {
		super();
	}
	public String getImg_id() {
		return img_id;
	}
	public void setImg_id(String img_id) {
		this.img_id = img_id;
	}
	public String getAlgorithm_name() {
		return algorithm_name;
	}
	public void setAlgorithm_name(String algorithm_name) {
		this.algorithm_name = algorithm_name;
	}
	public String getAlgorithm_type() {
		return algorithm_type;
	}
	public void setAlgorithm_type(String algorithm_type) {
		this.algorithm_type = algorithm_type;
	}
	public String getAlgorithm_tool() {
		return algorithm_tool;
	}
	public void setAlgorithm_tool(String algorithm_tool) {
		this.algorithm_tool = algorithm_tool;
	}
	public String getAlgorithm_language() {
		return algorithm_language;
	}
	public void setAlgorithm_language(String algorithm_language) {
		this.algorithm_language = algorithm_language;
	}
	public String getAlgorithm_parameter() {
		return algorithm_parameter;
	}
	public void setAlgorithm_parameter(String algorithm_parameter) {
		this.algorithm_parameter = algorithm_parameter;
	}
	public String getAlgorithm_data_format() {
		return algorithm_data_format;
	}
	public void setAlgorithm_data_format(String algorithm_data_format) {
		this.algorithm_data_format = algorithm_data_format;
	}
	public String getAlgorithm_method() {
		return algorithm_method;
	}
	public void setAlgorithm_method(String algorithm_method) {
		this.algorithm_method = algorithm_method;
	}
	
}
